function toggleLanguage() {
    let currentLang = localStorage.getItem('skyline_lang') || 'en';
    let newLang = (currentLang === 'en') ? 'ar' : 'en';
    localStorage.setItem('skyline_lang', newLang);
    applyLanguage(newLang);
}

function applyLanguage(lang) {
    document.documentElement.setAttribute('lang', lang);
    document.documentElement.setAttribute('dir', lang === 'ar' ? 'rtl' : 'ltr');
    
    // ترجمة العناصر
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) el.textContent = translations[lang][key];
    });

    // تحديث نص الزر
    const btnText = document.getElementById('lang-btn-text');
    if (btnText) btnText.textContent = translations[lang].lang_btn;

    // تحديث الخط
    document.body.style.fontFamily = lang === 'ar' ? "'Tajawal', sans-serif" : "'Inter', sans-serif";
    
    // إعادة بناء الأيقونات في حال تغير الاتجاه
    if (window.lucide) lucide.createIcons();
}

// التشغيل عند التحميل
document.addEventListener('DOMContentLoaded', () => {
    applyLanguage(localStorage.getItem('skyline_lang') || 'en');
});